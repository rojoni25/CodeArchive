.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB 'Input-1: $' 
MSG2 DB 0DH,0AH,'Input-2: $' 
MSG3 DB 0DH,0AH,'RESULT:$' 
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
LEA DX,MSG1
MOV AH,9
INT 21H

MOV AH,1
INT 21H
MOV BL,AL 
AND BL,0DFH


LEA DX,MSG3
MOV AH,9
INT 21H
MOV AH,2
MOV DL,BL
INT 21H

MOV AH,4CH
INT 21H


MAIN ENDP
END MAIN